export default function AdminSidebar({ email }: { email: string }) {
  const pathname = usePathname();
  const router = useRouter();

  const cerrarSesion = async () => {
    const supabase = createClient();
    await supabase.auth.signOut();
    router.push("/login");
    router.refresh();
  };

  return (
    <aside className="hidden lg:flex flex-col w-64 bg-steel-950 border-r border-steel-800 min-h-screen sticky top-0">
      <div className="p-6 border-b border-steel-800">
        <Link href="/admin">
          <Logo size="sm" />
        </Link>
        <p className="text-[10px] text-steel-500 uppercase tracking-wider mt-2">
          Panel administración
        </p>
      </div>

      <nav className="flex-1 p-3 space-y-1">
        {items.map((item) => {
          const activo =
            item.href === "/admin"
              ? pathname === "/admin"
              : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm transition-colors ${
                activo
                  ? "bg-brand/10 border border-brand/30 text-brand"
                  : "text-steel-300 hover:bg-steel-800/60 hover:text-white"
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="p-3 border-t border-steel-800">
        <div className="px-3 py-2 mb-2">
          <p className="text-[10px] text-steel-500 uppercase tracking-wider">
            Sesión
          </p>
          <p className="text-xs text-steel-300 truncate">{email}</p>
        </div>
        <button
          onClick={cerrarSesion}
          className="w-full flex items-center gap-3 px-3 py-2 rounded-lg text-sm text-steel-400 hover:bg-steel-800/60 hover:text-white transition-colors"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          Cerrar sesión
        </button>
        <Link
          href="/"
          className="mt-1 w-full flex items-center gap-3 px-3 py-2 rounded-lg text-xs text-steel-500 hover:text-steel-300 transition-colors"
        >
          ← Ver sitio público
        </Link>
      </div>
    </aside>
  );
}
